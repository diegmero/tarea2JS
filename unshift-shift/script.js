// ejercicio de unshift

let carro = ['color', 'marca', 'placa'];

carro.unshift('llantas', 'puertas') // incorporando 2 elementos al inicio del array

console.log(carro); //imprimiendo en consola en total 5 elementos

carro.shift(); // eliminando el primer elemento del array

console.log(carro); // imprimiendo en consola en total 4 elementos del array, gracias
